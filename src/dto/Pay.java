package dto;

public class Pay {
	String pay_code;
	String custom_id;
	String pay_type;
	int pay_price;
	String pay_day;
	String event_code;
	String open_code;
	String buy_code;
	public String getPay_code() {
		return pay_code;
	}
	public void setPay_code(String pay_code) {
		this.pay_code = pay_code;
	}
	public String getCustom_id() {
		return custom_id;
	}
	public void setCustom_id(String custom_id) {
		this.custom_id = custom_id;
	}
	public String getPay_type() {
		return pay_type;
	}
	public void setPay_type(String pay_type) {
		this.pay_type = pay_type;
	}
	public int getPay_price() {
		return pay_price;
	}
	public void setPay_price(int pay_price) {
		this.pay_price = pay_price;
	}
	public String getPay_day() {
		return pay_day;
	}
	public void setPay_day(String pay_day) {
		this.pay_day = pay_day;
	}
	public String getEvent_code() {
		return event_code;
	}
	public void setEvent_code(String event_code) {
		this.event_code = event_code;
	}
	public String getOpen_code() {
		return open_code;
	}
	public void setOpen_code(String open_code) {
		this.open_code = open_code;
	}
	public String getBuy_code() {
		return buy_code;
	}
	public void setBuy_code(String buy_code) {
		this.buy_code = buy_code;
	}
	
}
